function loadEngine(p) {
    p.size(600, 768);
    p.frameRate(60);

    textAlign = p.textAlign;
    fill = p.fill;
    text = p.text;
    PVector = p.PVector;
    color = p.color;
    width = p.width;
    Random = p.random;
    height = p.height;
    cos = p.cos;
    sin = p.sin;
    ellipse = p.ellipse;
}